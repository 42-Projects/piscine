#include "ft_btree.h"

void	btree_apply_prefix(t_btree *root, void (*applyf)(void *))
{
	if (root)
	{
		applyf(root->data);
		btree_apply_prefix(root->left);
		btree_apply_prefix(root->right);
	}
}
